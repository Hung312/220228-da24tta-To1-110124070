#include <stdio.h>
#include <math.h>

int main()
{
	double a=123.567, b=-12;
	printf ("fabs(%lf)= %lf\n",b,fabs(b));
	printf ("ceil(%lf)= %lf\n",a,ceil(a));
	printf ("floor(%lf)= %lf\n",a,floor(a));
	printf ("sqrt(%lf)= %lf\n",a,sqrt(a));
	printf ("pow(2,3)= %lf",pow(2,3));
	return 0;
}
